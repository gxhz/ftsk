package com.model;

public class SectionsInfo {
	private int Id;
	private String Name;
	private String Des;
	
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
    public String getDes() {
        return Des;
    }
    public void setDes(String des) {
        Des = des;
    }
}
