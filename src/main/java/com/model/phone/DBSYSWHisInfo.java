package com.model.phone;

public class DBSYSWHisInfo {
    
	private String time = "";
	private String name="";
	private double ksw = 0.0;
	private double sysw = 0.0;
	
    public String getTime() {
        return time;
    }
    public void setTime(String time) {
        this.time = time;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public double getKsw() {
        return ksw;
    }
    public void setKsw(double ksw) {
        this.ksw = ksw;
    }
    public double getSysw() {
        return sysw;
    }
    public void setSysw(double sysw) {
        this.sysw = sysw;
    }
	
	
}
